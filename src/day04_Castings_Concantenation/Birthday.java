package day04_Castings_Concantenation;

public class Birthday {

    public static void main(String[] args) {

        String name = "John",
                birthMonth = "April";
        int birthDay = 25,
                birthYear = 1995;

        System.out.println(name + " was born on "+birthMonth +
                "/"+birthDay+"/"+birthYear+".");

    }

}
